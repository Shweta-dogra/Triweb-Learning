//decimal
let n1 = 234;
console.log(n1); //234

//octal
let n2 = 0o234;
console.log(n2); //156   // octal to decimal

//binary
let n3 = 0b110;
console.log(n3); //6    binary to decimal

//hexadecimal
let n4 = 0x2A4;
console.log(n4);  //676 hex to decimal


//exponential numbers

let exp = 1E4;
console.log(exp); //10000


